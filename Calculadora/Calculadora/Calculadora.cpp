#include "Calculadora.h"

Calculadora::Calculadora()
{
}

Calculadora::~Calculadora()
{
}

float Calculadora::soma(float opA, float opB)
{
	return opA + opB;
}

float Calculadora::subtracao(float opA, float opB)
{
	return opA - opB;
}

float Calculadora::multiplicacao(float opA, float opB)
{
	return opA * opB;
}
